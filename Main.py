import numpy as np  


array = np.array([10, 20, 30, 40, 50])  


print("Array de NumPy:", array)

sum_array = np.sum(array) 
mean_array = np.mean(array) 
std_array = np.std(array) 

Resultado1 = f"El resultado del array es: {sum_array}"
Resultado2 = f"La media del array es: {mean_array}"
Resultado3 = f"La media del array es: {std_array}"

print(Resultado1)
print(Resultado2)
print(Resultado3)